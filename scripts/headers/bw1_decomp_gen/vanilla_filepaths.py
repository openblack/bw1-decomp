ROOMMATE_CONTAINED_CLASS_SUFFIXES = {
    "__AtomData",
    "__FlyingAtomData",
    "__ParentAtomData",
    "__AtomDataRipple",
    "__CollectionData",
    "__SubCollectionData",
    "__DebrisCollectionData",
    "__FlyingCollectionData",
    "__ParentCollectionData",
    "__Band",
}


def resolve_roommate(struct_name):
    for suffix in ROOMMATE_CONTAINED_CLASS_SUFFIXES:
        if struct_name.endswith(suffix):
            return resolve_roommate(struct_name.removesuffix(suffix))
    struct_name = demangle_type(struct_name)
    if "::" in struct_name:
        split = struct_name.split("::")[0]
        if not "<" in struct_name and not ">" in struct_name:
            return ROOMMATE_CLASS_MAP.get(split, split)
    match = re.search(rf"^({"|".join(LH_COLLECTION_TEMPLATES)})<([^<>\*]+)\*?>", struct_name)
    if match:
        struct_name = match.group(2)
    if '<' in struct_name and not "":
        split = struct_name.split('<')
        if "," not in split[1]:
            struct_name = split[0]
    return ROOMMATE_CLASS_MAP.get(struct_name, ROOMMATE_CLASS_MAP.get(struct_name.replace("::", "__"), struct_name))


