def filter_paths(path: BWPath) -> BWPath:
    # Define parts to remove
    parts_to_remove = {'debug', 'release', 'mfcdebug', 'mp'}
    # Convert to lower case and filter parts
    filtered_parts = [part.lower() for part in path.parts if part.lower() not in parts_to_remove]
    return BWPath(*filtered_parts)


def get_project_paths() -> set[BWPath]:
    relatives = map(lambda x: x.relative_to(BWPath(r"C:\dev")).parent, dev_filepaths)
    debug_release_stripped = map(filter_paths, relatives)

    return set(debug_release_stripped)


def map_projects_to_object_files() -> dict[BWPath, set[BWPath]]:
    projects = defaultdict(set)
    project_paths = get_project_paths()
    for path in dev_filepaths:
        filtered_path = filter_paths(path.relative_to(BWPath(r"C:\dev")).parent)
        object_file = path.stem + ".obj"
        if filtered_path in project_paths:
            projects[filtered_path].add(object_file)
    return dict(projects)

def get_object_file_base_names() -> set[str]:
    return {i.stem for i in dev_filepaths}

def resolve_vanilla_path(struct_name: str, header_version: bool) -> Path:
    roomate_class_name = resolve_roommate(struct_name)
    if roomate_class_name[0] == 'G' and roomate_class_name[1].isupper():
        roomate_class_name = roomate_class_name[1:]
        if roomate_class_name[0] == 'P' and roomate_class_name[1].isupper():
            roomate_class_name = roomate_class_name[1:]
    if roomate_class_name[1:3] == 'R_' and roomate_class_name[0].isupper():
        roomate_class_name = roomate_class_name[3:]
    return class_to_header_map.get(roomate_class_name)
